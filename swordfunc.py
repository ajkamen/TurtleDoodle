import turtle

def squaredrawing(x,y,length,r,g,b):
	d=turtle.Turtle()
	d.pu()
	d.speed(0)
	d.goto(x-100,y-100)
	d.pd
	d.fillcolor(r,g,b)
	d.begin_fill()
	for i in range (1,5,1):
		d.forward(length)
		d.left(90)
	d.hideturtle()
	d.end_fill()


